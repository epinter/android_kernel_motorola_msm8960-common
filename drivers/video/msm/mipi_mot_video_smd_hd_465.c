static int getElvssForGamma(int gammaIdx)
{
	int nits = 300 - gammaIdx*10;
	if (nits <= 100)
		return 3;
	if (nits <= 160)
		return 2;
	if (nits <= 200)
		return 1;
	return 0;
}


